public class java_17_relational_Logical {
    public static void main(String[] args) {
        //Logical Operators

         //AND OPERATOR--&&
        //OR OPERATOR--||(pipe operator)
        //NOT OPERATOR--!

         boolean a=true;
         boolean b=false;

        System.out.println(a&&b);
        System.out.println(a||b);
        System.out.println(!a);

    }
}
