public class java_16_conditionals {
    public static void main(String[] args) {
        int age=46;

        //if-else statement
        if(age>18){
            System.out.println("Yes");
        }
        else{
            System.out.println("No");
        }

        //if statement
        if (age>18){
            System.out.println("wow");
        }
    }
}
