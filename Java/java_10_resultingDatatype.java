public class java_10_resultingDatatype {
    public static void main(String[] args) {
        byte x=5;
        int y=6;
        short z=8;
        int n=y+z;
        System.out.println(x+n);
        int a=656+6;
        float b=6.46f+6;
        System.out.println(a);
        System.out.println(b);
    }
}
